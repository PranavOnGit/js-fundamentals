
//Array Methods

var arr = [10, 20, 30 , 40]
console.log(arr);

//POP Method

var a = arr.pop()
console.log('Poped Array :', arr);
console.log('Poped Array Element :', a);

//PUSH Method
arr.push(50)
console.log('Pushed Array : ', arr)

//SHIFT Method
var b = arr.shift()
console.log("Shifted Array: ", arr)
console.log("Shifted Array Element : ", b)

//UNSHIFT Method
arr.unshift(10)
console.log('Unshifted Array : ', arr)